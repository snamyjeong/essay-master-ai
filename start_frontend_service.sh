#!/usr/bin/env zsh
# 스크립트 실행을 위해 시스템에 설치된 zsh 쉘 환경을 사용하도록 선언합니다.
# start_frontend_service.sh - Conda 가상환경 내 npx를 찾아내고 의존성 자동 복구 기능이 추가된 고도화 버전입니다.

# 1. 경로 설정
# 현재 작업 디렉토리(pwd)를 기준으로 메인 Expo 모바일 프로젝트가 위치한 절대 경로를 MAIN_FRONT_DIR 변수에 할당합니다.
MAIN_FRONT_DIR="$(pwd)/frontend-mobile"
# 현재 작업 디렉토리(pwd)를 기준으로 백업용 Next.js 프로젝트가 위치한 절대 경로를 BACKUP_FRONT_DIR 변수에 할당합니다.
BACKUP_FRONT_DIR="$(pwd)/frontend-nextjs"

# 2. 기존 세션 정리
# 스크립트 실행 시 사용자에게 기존 프런트엔드 세션을 정리(초기화)하고 있음을 알리는 안내 문구를 터미널에 출력합니다.
echo "🛑 기존 모든 프런트엔드 세션 정리 중..."
# 이름이 'frontend'로 지정된 tmux 세션이 있다면 강제로 종료(kill)하며, 이때 발생하는 에러 메시지는 2>/dev/null을 통해 화면에 보이지 않게 숨깁니다.
tmux kill-session -t frontend 2>/dev/null
# 이름이 'frontend-backup'으로 지정된 백업 tmux 세션이 있다면 강제로 종료하며, 마찬가지로 에러 메시지를 숨깁니다.
tmux kill-session -t frontend-backup 2>/dev/null
# 프로세스가 완전히 소멸하고 시스템 자원이 반환될 수 있도록 스크립트 실행을 1초간 일시 정지(대기)합니다.
sleep 1

# 3. [핵심 로직] 메인 프로젝트 확인 및 자동화 처리
# MAIN_FRONT_DIR 변수에 지정된 경로가 실제로 존재하는 디렉토리인지(-d) 조건문으로 검사합니다.
if [ -d "$MAIN_FRONT_DIR" ]; then

    # 3.5 [자동화] 터널링 패키지 검증 및 조건부 설치
    # 터널링 패키지의 설치 상태를 확인한다는 안내 메시지를 터미널에 출력합니다.
    echo "🔍 Expo 터널링 패키지(@expo/ngrok) 설치 상태를 검증합니다..."
    
    # 프론트엔드 프로젝트 내의 node_modules 디렉토리 아래에 @expo/ngrok 폴더가 존재하지 않는지(! -d) 검사합니다.
    if [ ! -d "$MAIN_FRONT_DIR/node_modules/@expo/ngrok" ]; then
        # 폴더가 없다면 패키지가 설치되지 않은 것으로 판단하고 자동 설치를 시작한다는 경고 문구를 출력합니다.
        echo "⚠️  @expo/ngrok 패키지가 누락되어 있습니다. 자동 설치를 진행합니다..."
        
        # 괄호 '()'를 사용하여 서브쉘(Sub-shell)을 엽니다. 서브쉘 안에서의 디렉토리 이동이나 환경 변수 변경은 메인 스크립트에 영향을 주지 않아 안전합니다.
        (
            # 서브쉘 내부에서 작업 디렉토리를 메인 프론트엔드 폴더로 이동합니다.
            cd "$MAIN_FRONT_DIR"
            # npm 명령어를 정상적으로 실행하기 위해 conda의 zsh 쉘 훅(hook)을 평가하여 초기화합니다.
            eval "$(conda shell.zsh hook)"
            # 필요한 node 환경이 세팅되어 있는 'essay-master' 콘다 가상환경을 활성화합니다.
            conda activate essay-master
            # 현재 프로젝트의 개발 의존성(--save-dev)으로 @expo/ngrok 패키지를 npm을 통해 로컬로 설치합니다.
            npm install @expo/ngrok --save-dev
        # 서브쉘의 작업 영역을 종료합니다. 메인 스크립트의 경로는 변경되지 않은 상태로 유지됩니다.
        )
        
        # 패키지 다운로드 및 설치가 무사히 끝났음을 사용자에게 알리는 성공 메시지를 출력합니다.
        echo "✅ 터널링 패키지 자동 설치가 완료되었습니다."
    # 앞선 if문에서 패키지 폴더가 이미 존재한다고 판단되었을 경우 실행될 분기입니다.
    else
        # 이미 패키지가 설치되어 있으므로 불필요한 설치 과정을 건너뛰고 정상 준비되었음을 알리는 메시지를 출력합니다.
        echo "✅ 터널링 패키지가 정상적으로 준비되어 있습니다."
    # 의존성 패키지 검사 및 자동 설치를 담당하는 조건문의 종료를 나타냅니다.
    fi

    # Expo 모바일 서버 서비스를 백그라운드 환경에서 가동한다는 안내 메시지를 터미널에 출력합니다.
    echo "🚀 [Main UI] Expo Mobile 서비스를 백그라운드에서 기동합니다."
    # 'frontend'라는 이름의 새로운 tmux 세션을 백그라운드 분리 모드(-d)로 생성하며, 시작 디렉토리(-c)를 MAIN_FRONT_DIR로 설정합니다.
    tmux new-session -d -s frontend -c "$MAIN_FRONT_DIR"
    
    # 4. [환경 보강] Conda 환경 전이 및 서버 실행
    # 방금 생성한 tmux 'frontend' 세션 내부에 conda 초기화 훅 명령어를 타이핑하고 엔터(C-m)를 치도록 명령을 보냅니다.
    tmux send-keys -t frontend "eval \"\$(conda shell.zsh hook)\"" C-m
    # 해당 세션 안에서 'essay-master' 콘다 가상환경을 실행하여 서버 구동에 필요한 라이브러리를 로드하도록 엔터(C-m)와 함께 명령을 전송합니다.
    tmux send-keys -t frontend "conda activate essay-master" C-m
    
    # 서버 실행 안정성을 높이기 위해 가상환경 내부에 위치한 npx 바이너리의 절대 경로를 NPX_EXE 변수에 저장합니다.
    NPX_EXE="/home/snamy78/miniconda3/envs/essay-master/bin/npx"
    # 변수에 저장된 npx 바이너리의 경로가 올바른지 디버깅 및 확인 목적으로 화면에 출력합니다.
    echo "🔍 npx 경로 확인: $NPX_EXE"
    
    # tmux 세션에 Expo 개발 서버를 캐시 초기화(-c) 옵션과 외부 접속을 위한 터널링(--tunnel) 옵션과 함께 실행하도록 엔터(C-m)를 전송합니다.
    tmux send-keys -t frontend "$NPX_EXE expo start -c --tunnel" C-m
    
    # 5. 접속 URL 추출 시퀀스 (최대 60초 대기)
    # 터널링 서버가 열리고 ngrok 주소가 발급되기까지 시간이 걸리므로 사용자에게 대기하라는 안내 문구를 출력합니다.
    echo "⏳ 보안 환경을 위한 접속 주소 추출 중... (잠시만 기다려주세요)"
    # for 반복문을 사용하여 1부터 60까지 증가시키며 최대 60회(약 60초) 동안 주소 추출을 지속적으로 시도합니다.
    for i in {1..60}; do
        # 반복 주기를 1초로 설정하여 CPU 리소스를 낭비하지 않고 로그가 충분히 생성될 여유 시간을 확보합니다.
        sleep 1
        # tmux 세션의 출력 내용을 텍스트로 캡처(capture-pane)하되, 너비 제한으로 인한 줄바꿈 문제를 방지(-J)하고 정규표현식(grep)으로 exp:// 주소만 필터링한 뒤 맨 마지막 줄(tail -n 1)을 EXPO_URL 변수에 저장합니다.
        EXPO_URL=$(tmux capture-pane -J -pt frontend -S - | grep -o 'exp://[a-zA-Z0-9.-]*-[0-9]*.exp.direct[a-zA-Z0-9./?=&-]*' | tail -n 1)
        
        # 추출된 결과값을 담은 EXPO_URL 변수가 비어있지 않다면(! -z), 즉 주소를 성공적으로 찾아냈다면 실행되는 분기입니다.
        if [ ! -z "$EXPO_URL" ]; then
            # 화면의 시각적 피드백(진행 바) 줄을 끊고 새 줄(\n)에 주소 추출에 성공했다는 긍정적인 메시지를 출력합니다.
            echo "\n✅ 접속 주소 추출 성공!"
            # 더 이상 주소를 찾기 위해 대기할 필요가 없으므로 for 반복문을 즉각 탈출(break)합니다.
            break
        # 주소 성공 여부를 검사하는 if 조건문의 끝입니다.
        fi
        
        # 주소를 아직 찾지 못했다면 터미널에서 줄바꿈 없이(-n) 사각형(■) 기호를 이어 붙여 로딩 프로그레스 바와 같은 시각적 효과를 줍니다.
        echo -n "■"
    # 최대 60초 동안 주소 추출을 대기하며 반복하는 for 루프의 종료 지점입니다.
    done

# 만약 초기 조건문에서 메인 프론트엔드 폴더(MAIN_FRONT_DIR)를 찾지 못했을 경우 실행되는 예외 처리 분기입니다.
else
    # 메인 폴더가 없으므로 백업용 Next.js 서비스를 대체 기동하겠다는 경고성 안내 메시지를 터미널에 출력합니다.
    echo "⚠️  메인 폴더가 없습니다. 백업 서비스를 기동합니다."
    # 'frontend-backup'이라는 이름의 새로운 tmux 세션을 백업 프로젝트 디렉토리(-c "$BACKUP_FRONT_DIR")에서 백그라운드 모드(-d)로 생성합니다.
    tmux new-session -d -s frontend-backup -c "$BACKUP_FRONT_DIR"
    # 백업 세션에 Next.js 개발 서버를 실행하는 'npm run dev' 명령어와 함께 실행 키(C-m)를 전송합니다.
    tmux send-keys -t frontend-backup "npm run dev" C-m
# 메인 프로젝트 존재 유무를 확인하는 가장 큰 핵심 로직 조건문의 종료 지점입니다.
fi

# 6. 최종 가동 결과 보고
# 스크립트 실행 과정과 결과 요약 부분을 시각적으로 명확히 분리하기 위해 점선으로 된 구분선을 출력합니다.
echo "----------------------------------------------------------------"
# 사용자에게 현재 시스템 백그라운드에서 실행되고 있는 프런트엔드 관련 tmux 세션 리스트를 조회해 주겠다는 안내 문구입니다.
echo "🔍 현재 실행 중인 프런트엔드 tmux 세션 리스트:"
# 시스템의 전체 tmux 세션 목록(tmux ls)을 가져온 후, 그 중에서 'frontend'라는 단어가 포함된 줄만 걸러내어(grep) 화면에 표시합니다.
tmux ls | grep frontend

# 다시 한번 텍스트 레이아웃 정리를 위해 점선으로 된 구분선을 출력합니다.
echo "----------------------------------------------------------------"
# 스크립트가 모든 명령 지시를 마치고 백그라운드 서비스들이 성공적으로 실행을 시도하고 있다는 최종 상황 안내 메시지입니다.
echo "✨ 모든 서비스가 정상 궤도 진입을 시도 중입니다."

# 앞선 반복문에서 추출을 시도했던 터널링 주소가 EXPO_URL 변수에 정상적으로 저장되어 비어있지 않은지(! -z) 최종적으로 확인하는 조건문입니다.
if [ ! -z "$EXPO_URL" ]; then
    # 변수에 값이 있다면, 사용자에게 태블릿이나 스마트폰의 Expo Go 앱 주소창에 입력해야 할 보안 주소임을 알립니다.
    echo "🔗 [보안 접속용 URL] 태블릿 주소창에 아래 주소를 입력하세요:"
    # 실제로 외부에서 접속 가능한 ngrok 기반의 exp:// 터널링 URL 값을 터미널 화면에 노출시킵니다.
    echo "👉 $EXPO_URL"
    # 출력 내용을 깔끔하게 구획하기 위해 점선 형태의 가로 구분선을 추가로 출력합니다.
    echo "----------------------------------------------------------------"
    # 주소 출력만으로 부족하거나 QR 코드를 스캔하고 싶을 때 직접 세션 터미널로 진입하는 명령어 가이드를 제공합니다.
    echo "📱 상세 로그 및 QR 코드 확인: tmux attach -t frontend"

# 주소 변수가 비어있는 경우, 즉 60초가 지났음에도 에러나 타임아웃으로 인해 주소를 추출하지 못했을 때 실행되는 분기입니다.
else
    # 주소를 자동으로 가져오는 프로세스가 실패했으며, 사용자가 직접 에러 로그를 확인해야 함을 경고하는 메시지입니다.
    echo "❌ 주소 추출 실패. 직접 로그 확인이 필요합니다."
    # 실패 원인 분석 섹션을 시각적으로 분리하기 위해 점선 구분선을 터미널에 출력합니다.
    echo "----------------------------------------------------------------"
    # 실패 상황에 대한 편의성을 위해, 세션에 직접 들어가지 않고도 터미널에서 즉시 마지막 로그를 확인할 수 있도록 안내하는 문구입니다.
    echo "🚨 [실패 원인 분석용 마지막 로그 10줄]"
    # tmux capture-pane 명령으로 frontend 세션의 현재 화면 텍스트를 줄바꿈 보정(-J)하여 가져온 뒤, tail -n 10 명령으로 맨 마지막 10줄만 잘라서 에러 원인을 터미널에 즉시 표시합니다.
    tmux capture-pane -J -pt frontend | tail -n 10
    # 제공된 10줄의 로그만으로 부족하여 전체 로그를 확인하거나 상호작용(예: 프롬프트 승인)이 필요할 때 사용할 접속 명령어를 안내합니다.
    echo "📝 확인 명령어: tmux attach -t frontend"
# URL 추출 성공 여부에 따른 최종 결과 안내 분기 처리를 종료합니다.
fi

# 사용자가 tmux 세션(백그라운드 터미널)에 진입한 후, 서버를 끄지 않고 안전하게 원래 터미널로 빠져나오기(Detach) 위한 단축키 사용 팁을 출력합니다.
echo "💡 팁: 로그 화면 탈출은 [Ctrl + B] 누른 후 [D] 입니다."
# 스크립트의 모든 실행이 종료되었음을 나타내는 마지막 점선 형태의 텍스트 구분선입니다.
echo "----------------------------------------------------------------"